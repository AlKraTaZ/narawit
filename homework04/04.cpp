#include <iostream>
using namespace std;

int main(){
	float x = 25.25;
	float y = 18.98;
	cout << "Print the sum of two numbers: " << endl;
	cout << "----------------------------: " << endl;
	cout << "The result of addition between " << x << " and " << y << " is "<< x+y << endl;
	cout << "The result of subtraction between " << x << " and " << y << " is "<< x-y << endl;
	cout << "The result of multiplication between " << x << " and " << y << " is "<< x*y << endl;
	cout << "The result of division between " << x << " and " << y << " is "<< x/y << endl;
	return 0;
}
